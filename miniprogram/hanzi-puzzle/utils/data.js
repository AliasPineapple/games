// ══ 字根→能与哪些偏旁组字（从 RADICAL_DB 自动推导）══
const CHAR_DB = (() => {
  const db = {};
  for (const [radical, entries] of Object.entries(RADICAL_DB)) {
    for (const [root, hanzi] of Object.entries(entries)) {
      if (!db[root]) db[root] = {};
      db[root][radical] = hanzi;
    }
  }
  return db;
})();

